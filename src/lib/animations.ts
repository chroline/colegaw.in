const container = {
  hidden: {},
  visible: {
    transition: {
      staggerChildren: 0.1,
    },
  },
};

const item = {
  hidden: { opacity: 0, y: 20, filter: "blur(10px)" },
  visible: {
    opacity: 1,
    y: 0,
    filter: "blur(0px)",
    transition: {
      duration: 0.5,
      ease: "easeOut",
    },
  },
};

function delayItem(delay: number) {
  return { ...item, visible: { ...item.visible, transition: { ...item.visible.transition, delay } } };
}

export { container, item, delayItem };
